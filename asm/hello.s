== Wiki ITSOS ==
Lo scopo del progetto '''Wiki ITSOS''' è quello di ottenere una raccolta di tutti i documenti tecnici creati nel corso degli anni dagli studenti delle classi informatiche dell' '''ITSOS Marie Curie'''.

Abbiamo scelto di adottare la filosofia ''Wiki'' in modo che chiunque sia libero di partecipare. Al momento il sito esiste da poco e quindi risulta quasi vuoto e privo di documenti.


